			    <h3>- Sinopsis Buku</h3>
			</ul>
				<p>Novel yang telah diangkat menjadi film ini menceritakan tradisi masyarakat Minang pada zaman itu. Penulis yaitu Prof. Dr. Haji Abdul Malik Karim Amrullah atau lebih dikenal dengan Buaya Hamka mengkritik tradisi Minang terutama perkawinan paksa melalui novel ini. Novel ini  menceritakan kisah cinta yang setia antara dua insan.</p>
				<p>Tokoh yang bernama Zainuddin adalah seorang yatim piatu yang memiliki nasib kurang beruntung. Ibu Zainuddin adalah orang asli Makasar yang menikah dnegan orang asli Minang. Oleh karena itu Zainuddin terbuang dari Makasar yang merupakan tanah kelahirannya. Pada suatu ketika Zainuddin mengunjungi Padang Panjang, namun disana ia dianggap sebagai orang asing.</p>
				<p>Kemudian Zainuddin bertemu dengan seorang gadis dari keluarga leluhur terpandang yang bernama Hayati. Zainuddin dan Hayati saling jatuh cinta, namun ibunya Hayati sangat tidak merestui hingga mengusir Zainuddin dari Pandang Pandang. meski Zainuddin pergi meninggalkan Hayati, tetapi mereka berjanji untuk tetap setia.</p>
